def add(a,b,c,d):
    return a+b+c+d
def subtrsct(a,b,c,d):
    return a-b-c-d
def multiply(a,b,c,d):
    return a*b*c*d
def divide(a,b,c,d):
    return a/b/c/d
# area of rectangle
def area_rectangle(lenght,breath):
    return lenght*breath
# area of square 
def area_square(side):
    return side*side
def area_triangle(base,height):
    return 0.5*base*height
    